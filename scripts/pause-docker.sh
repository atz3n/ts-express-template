#!/bin/bash

# Pauses the docker-compose project

###################################################################################################
# CONFIGURATION
###################################################################################################

PROJECT_NAME="greeter"


###################################################################################################
# DEFINES
###################################################################################################

HERE="$(PWD)/$(dirname $0)"


###################################################################################################
# MAIN
###################################################################################################

cd ${HERE}/../docker
if [ $(uname) == Linux ]; then
    sudo docker-compose -p ${PROJECT_NAME} stop
else 
    docker-compose -p ${PROJECT_NAME} stop
fi