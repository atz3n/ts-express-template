#!/bin/bash

# Runs the docker-compose project

###################################################################################################
# CONFIGURATION
###################################################################################################

PROJECT_NAME="greeter"


###################################################################################################
# DEFINES
###################################################################################################

HERE="$(pwd)/$(dirname $0)"


###################################################################################################
# MAIN
###################################################################################################

cd ${HERE}/../docker
if [ $(uname) == Linux ]; then
    sudo docker-compose -p ${PROJECT_NAME} up -d
else 
    docker-compose -p ${PROJECT_NAME} up -d
fi