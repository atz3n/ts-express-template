#!/bin/bash

# Stops and removes the docker-compose project

###################################################################################################
# CONFIGURATION
###################################################################################################

PROJECT_NAME="greeter"


###################################################################################################
# DEFINES
###################################################################################################

HERE="$(pwd)/$(dirname $0)"


###################################################################################################
# MAIN
###################################################################################################

cd ${HERE}/../docker
if [ $(uname) == Linux ]; then
    sudo docker-compose -p ${PROJECT_NAME} down
else 
    docker-compose -p ${PROJECT_NAME} down
fi